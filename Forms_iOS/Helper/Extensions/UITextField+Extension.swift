import UIKit
import SkyFloatingLabelTextField

extension SkyFloatingLabelTextField {

    func configureTextField() {
        self.selectedLineColor = .lightGray
        self.selectedTitleColor = .lightGray
        self.errorColor = .red
        self.textColor = .themeBlueColor
    }

}
