import UIKit

extension UIColor {
    
    public class var themeBlueColor: UIColor {
        return UIColor(red: 44/255, green: 78/255, blue: 104/255, alpha: 1)
    }
    
    public class var placeholderColor: UIColor {
        return UIColor(red: 130/255, green: 130/255, blue: 130/255, alpha: 1)
    }
    
    open class var tableBackgroundColor:UIColor {
        return UIColor(red: 239/256, green: 239/256, blue: 244/256, alpha: 1)
    }
}
